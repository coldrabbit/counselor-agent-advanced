TASK_NAMESPACE = uuid.UUID("af8a908a-7a6c-4b7b-8f49-01667d6b37f7")


def _action_params(action_type: str, title: str) -> dict[str, str]:
    if action_type == "notice":
        return {
            "event": title,
            "time": "",
            "location": "",
            "participants": "全体学生",
        }
    if action_type == "talk":
        return {
            "event": title,
            "time": "",
            "location": "",
            "participants": "重点关注学生",
        }
    return {
        "event": title,
        "time": "",
        "location": "",
        "participants": "",
    }


def _build_seed(month: int, category: str, title: str, description: str) -> dict[str, object]:
    action_type, action_label = CATEGORY_ACTIONS[category]
    task_id = uuid.uuid5(TASK_NAMESPACE, f"{month}:{category}:{title}")
    return {
        "id": str(task_id),
        "month": month,
        "category": category,
        "title": title,
        "description": description,
        "action_type": action_type,
        "action_label": action_label,
        "action_params": _action_params(action_type, title),
    }


MONTHLY_TASK_SEEDS = [
    _build_seed(month, category, title, description)
    for month, items in MONTHLY_WORK_ITEMS.items()
    for category, title, description in items
]


def seed_monthly_tasks(db: Session) -> int:
    existing_ids = set(db.scalars(select(MonthlyTask.id)).all())
    tasks = [
        MonthlyTask(**item)
        for item in MONTHLY_TASK_SEEDS
        if str(item["id"]) not in existing_ids
    ]
    db.add_all(tasks)
    db.commit()
    return len(tasks)


def main() -> None:
    db = SessionLocal()
    try:
        inserted = seed_monthly_tasks(db)
        print(f"monthly_tasks seed inserted: {inserted}")
    finally:
        db.close()


if __name__ == "__main__":
    main()
